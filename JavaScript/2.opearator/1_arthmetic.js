/**
 *  사칙연산 : +, -, *, /, %, **
 */
console.log(3 + 5);
console.log(3 - 5);
console.log(3 * 5);
console.log(3 / 5);
console.log(3 % 5);
console.log(3 ** 5);

// 짝수, 홀수 선택 : %
let a = 101;
a = 100;
console.log((a % 2) ? "홀수" : "짝수");

// 접합연산자 : +
console.log(10 + 20);
console.log('10' + 20);
console.log('합계 = ' + a);
console.log(`합계 = ${a}`);
